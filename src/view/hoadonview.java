    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        String idkh = txtmakh.getText();
        String idnv = txtidnv.getText();
        String ma = txtma.getText();
        int tinhtrang = Integer.parseInt(txtinhtrang.getText());
        String namenguoinhan = txtnguoinhan.getText();
        String sdt = txtsdt.getText();
        Date ngtao = new Date();
        Date ngthanhtoan = new Date();
        Date ngship = new Date();
        Date ngnhan = new Date();
        String diachi = txtdiachi.getText();
        hoadon hd = listhoadon.get(row);
        String ma1=txtma.getText();
        hoadon dh = new hoadon(idkh, idnv, ma, ngtao, ngthanhtoan, ngship, ngnhan, tinhtrang, namenguoinhan, diachi, sdt);
        JOptionPane.showMessageDialog(this, hdmpl.update(dh, ma1));
        listhoadon = hdmpl.getall();
        dodate(listhoadon);
    }//GEN-LAST:event_jButton2ActionPerformed

