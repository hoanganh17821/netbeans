    public void dodate(List<hoadon> list) {
        model.setRowCount(0);
        for (hoadon a : list) {
            model.addRow(a.toaddrow());
        }
    }

    public void click(int index) {
        hoadon hd = listhoadon.get(index);
        txtmakh.setText(hd.getIdkh());
        txtidnv.setText(hd.getIdnv());
        txtma.setText(hd.getMa());
        txtngaytao.setText(hd.getNgaytao() + "");
        txtngaynhan.setText(hd.getNgaynhan() + "");
        txtngayship.setText(hd.getNgayship() + "");
        txtngaythanhtoan.setText(hd.getNgaythanhtoan() + "");
        txtinhtrang.setText(hd.getTinhtrang() + "");
        txtnguoinhan.setText(hd.getTennguoinhan());
        txtdiachi.setText(hd.getDiachi());
        txtsdt.setText(hd.getSdt());
    }

    public void add() {
        String idkh = txtmakh.getText();
        String idnv = txtidnv.getText();
        String ma = txtma.getText();
        int tinhtrang = Integer.parseInt(txtinhtrang.getText());
        String namenguoinhan = txtnguoinhan.getText();
        String sdt = txtsdt.getText();
        Date ngtao = new Date();
        Date ngthanhtoan = new Date();
        Date ngship = new Date();
        Date ngnhan = new Date();
        String diachi = txtdiachi.getText();
        hoadon dh = new hoadon(idkh, idnv, ma, ngtao, ngthanhtoan, ngship, ngnhan, tinhtrang, namenguoinhan, diachi, sdt);
        JOptionPane.showMessageDialog(this, hdmpl.add(dh));
        listhoadon = hdmpl.getall();
        dodate(listhoadon);

    }

