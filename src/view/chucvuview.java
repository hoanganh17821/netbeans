    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui Long Chon 1 Dong Table");
        }
        String id = txtid.getText();
        String ma = txtma.getText();
        String name = txtname.getText();
        chucvu cv1 = listchucvu.get(row);
        String id1 = cv1.getId();
        chucvu cn = new chucvu(ma, name);
        JOptionPane.showMessageDialog(this, cvmpl.update(cn, id1));
        listchucvu = cvmpl.getall();
        dodate(listchucvu);
    }//GEN-LAST:event_jButton2ActionPerformed

