    public void dodate(List<chucvu> list) {
        model.setRowCount(0);
        for (chucvu a : list) {
            model.addRow(a.toaddrow());
        }
    }
    public void clear() {
   txtid.setText("");
   txtma.setText("");
   txtname.setText("");
    }
    
    public void click(int index) {
        chucvu cv = listchucvu.get(index);
        txtid.setText(cv.getId());
        txtma.setText(cv.getMachucvu());
        txtname.setText(cv.getNamechucvu());
    }

