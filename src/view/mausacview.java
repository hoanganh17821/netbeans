    public void dodate(List<mausac> listshow) {
        model.setRowCount(0);
        for (mausac a : listshow) {
            model.addRow(a.toaddrow());
        }
    }

    public void click(int index) {
        mausac ms = listmausac.get(index);
        txtid.setText(ms.getId());
        txtma.setText(ms.getMams());
        txtname.setText(ms.getTenmausac());
    }

