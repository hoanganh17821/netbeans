    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = table.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui Long Chon 1 Dong Tren Table");
        }
        String ma = txtma.getText();
        String name = txtname.getText();
        mausac ms1 = listmausac.get(row);
        String ma1=ms1.getMams();
        mausac ms = new mausac(ma, name);
        JOptionPane.showMessageDialog(this, mspml.update(ms, ma1));
        listmausac=mspml.getall();
        dodate(listmausac);

    }//GEN-LAST:event_jButton3ActionPerformed

