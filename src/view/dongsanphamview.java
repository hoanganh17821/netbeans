    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       int row=table.getSelectedRow();
       if(row==-1) {
          JOptionPane.showMessageDialog(this, "Vui Long Chon Dong De Delete");
       }
      dongsanpham dsp1=listdongsp.get(row);
       String ma1=dsp1.getMaDsp();
        String ma=txtma.getText();
        String name=txtname.getText();
        dongsanpham dsp= new dongsanpham(ma, name);
        JOptionPane.showMessageDialog(this, spmpl.updatedongsanpham(dsp, ma1));
        listdongsp=spmpl.getall();
        dodate(listdongsp);
    }//GEN-LAST:event_jButton2ActionPerformed

