    public void dodate(List<dongsanpham> listshow) {
         model.setRowCount(0);
         for (dongsanpham a : listshow) {
            model.addRow(a.toaddrow());
        }
    }
    
    public void click(int index) {
        dongsanpham dsp= listdongsp.get(index);
        txtid.setText(dsp.getIdDsp());
        txtma.setText(dsp.getMaDsp());
        txtname.setText(dsp.getTendongsanpham());
        
    }
    
